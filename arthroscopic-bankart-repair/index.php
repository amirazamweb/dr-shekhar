                    <h3>What is a Bankart Lesion?</h3>
                   
                    <p>A Bankart lesion occurs when the labrum (a ring of cartilage that stabilizes the shoulder joint) is torn due to a dislocation. This damage makes the shoulder more prone to recurrent dislocations, leading to pain, weakness, and restricted movement. Athletes, especially those involved in contact sports, and individuals who have suffered a traumatic shoulder injury are at higher risk of developing this condition.</p>

                    <h3>What is Arthroscopic Bankart Repair?</h3>
                    <p>
                    Arthroscopic Bankart Repair is a minimally invasive procedure that involves repairing the torn labrum using an arthroscope (a small camera) and specialized surgical instruments. The torn labrum is reattached to the glenoid (shoulder socket) using small anchors and sutures, restoring the stability of the shoulder joint. <br><br>
                    Unlike traditional open surgery, arthroscopic repair requires only small incisions, leading to a quicker recovery, less pain, and a lower risk of complications.
                    </p>

