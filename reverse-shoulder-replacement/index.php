                            </li>
                            <li>
                            <span style="font-weight: 600;">Reversing the Joint Structure –</span> The normal ball-and-socket anatomy is reversed, with a metal ball placed on the shoulder blade (glenoid) and a plastic socket implanted in the arm bone (humerus).
