                    <p>
                    This procedure is recommended for individuals who have:
                        <ul>
                            <li>
                            Severe fractures of the humeral head that cannot be repaired.
                            </li>
                            <li>
                            Avascular necrosis (bone death) due to loss of blood supply to the humeral head.
                            </li>
                            <li>
                            Severe arthritis with extensive damage to the humeral head but a well-preserved socket.
                            </li>
                            <li>
                            Failed previous shoulder surgeries that require joint reconstruction.
                            </li>
                        </ul>
                    </p>

                    <h3>How is the Procedure Performed?</h3>
                    <p>
                        <ul>
                            <li>
                            <span style="font-weight: 600;">Anesthesia –</span> The patient is given general or regional anesthesia.
                            </li>
                            <li>
                            <span style="font-weight: 600;">Damaged Bone Removal –</span> The humeral head (ball of the shoulder joint) is removed while preserving the socket.
                            </li>
                            <span style="font-weight: 600;">Implant Fixation –</span> A metal prosthetic ball is fixed into the upper arm bone (humerus) using a stem.
                            </li>
                            </li>
                            <span style="font-weight: 600;">Closure and Recovery –</span> The joint is tested for smooth motion, the incisions are closed, and a structured rehabilitation plan is initiated.
                            </li>
                        </ul>
