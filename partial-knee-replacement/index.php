                    <h3>What is Partial Knee Replacement?</h3>

                    <p>Partial Knee Replacement (Unicompartmental Knee Replacement) is a surgical procedure in which only the damaged portion of the knee joint is replaced with an artificial implant while preserving the healthy bone, cartilage, and ligaments. This procedure is ideal for patients with arthritis limited to one compartment of the knee, providing relief while maintaining natural knee movement.</p>

                    <h3>Who is a Candidate for Partial Knee Replacement?</h3>

                    <p>
                    This procedure is suitable for individuals who:
                    <ul>
                        <li>Have osteoarthritis affecting only one part of the knee (medial, lateral, or patellofemoral)</li>
                        <li>Experience chronic knee pain and stiffness despite medication and therapy</li>
                        <li>Have intact knee ligaments (ACL, PCL) and a stable knee joint</li>
                        <li>Want a faster recovery and less invasive solution compared to total knee replacement</li>
                    </ul>
                    </p>

                    <h3>Benefits of Partial Knee Replacement</h3>
                    <p>
                    <ul>
                        <li><span style="font-weight: 600;">Minimally Invasive Surgery –</span> Smaller incisions lead to less tissue damage and quicker healing.</li>
                        <li><span style="font-weight: 600;">Faster Recovery –</span> Patients typically recover within 4 to 6 weeks, much faster than total knee replacement.</li>
                        <li><span style="font-weight: 600;">Natural Knee Movement –</span> Since ligaments and healthy bone are preserved, patients experience better joint flexibility.</li>
                        <li><span style="font-weight: 600;">Less Pain & Scarring –</span> Smaller incisions reduce post-surgical discomfort and scars.</li>
                        <li><span style="font-weight: 600;">Longer-Lasting Results –</span> Advanced implants provide durable and long-term relief from knee pain.</li>
                    </ul>
                    </p>

                    <h3>Surgical Procedure for Partial Knee Replacement</h3>
                    <p>
                    <ul>
                        <li><span style="font-weight: 600;">Pre-Surgical Evaluation –</span> Detailed examination, X-rays, and MRI scans to assess knee damage.</li>
                        <li><span style="font-weight: 600;">Anesthesia & Incision –</span> A small incision is made to access the damaged knee area.</li>
                        <li><span style="font-weight: 600;">Damaged Tissue Removal –</span> The worn-out cartilage and bone are carefully removed.</li>
                        <li><span style="font-weight: 600;">Implant Placement –</span> A high-quality prosthetic implant is positioned in place of the damaged cartilage.</li>
                        <li><span style="font-weight: 600;">Closure & Recovery –</span> The incision is closed, and post-operative care begins.</li>
                    </ul>
                    </p>

                    <h3>Recovery & Rehabilitation</h3>
                    <p>
                    Patients undergoing partial knee replacement with Dr. Shekhar Srivastav benefit from a comprehensive rehabilitation plan, ensuring a smooth and quick recovery.
                    <ul>
                        <li><span style="font-weight: 600;">Walking Support:</span> Patients can start walking with support within 24 hours</li>
                        <li><span style="font-weight: 600;">Physiotherapy:</span> Strengthening exercises begin within a few days</li>
                        <li><span style="font-weight: 600;">Full Recovery:</span> Most patients resume normal activities within 4-6 weeks</li>
                    </ul>
