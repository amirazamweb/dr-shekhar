                    <h3>What is Arthroscopic Subacromial Decompression?</h3>
                   
                    <p>Arthroscopic Subacromial Decompression is a keyhole surgery performed using an arthroscope (a small camera) and specialized instruments to remove bone spurs or inflamed tissue from the subacromial space. This reduces pressure on the rotator cuff tendons, alleviating pain and restoring mobility.</p>

                    <h3>Who Needs Arthroscopic Subacromial Decompression?</h3>
                    <p>
                    Dr. Shekhar Srivastav may recommend ASD for individuals experiencing:
                        <ul>
                            <li>
                            Persistent shoulder pain that does not respond to medications, physiotherapy, or steroid injections.
                            </li>
                            <li>
                            Shoulder impingement syndrome, where overhead movements cause pain due to tendon compression.
                            </li>
                            <li>
                            Rotator cuff irritation or bursitis, leading to inflammation and limited movement.
                            </li>
                            <li>
                            Difficulty lifting the arm, especially when reaching above the head.
                            </li>
                            <li>
                            Pain that worsens at night, affecting sleep quality.
                            </li>
                        </ul>
                    </p>

                    <h3>The Procedure: Step-by-Step</h3>
                     <p>
                        <ul>
                            <li>
                            <span style="font-weight: 600;">Anesthesia:</span> The procedure is performed under general or regional anesthesia.
                            </li>
                            <li>
                            <span style="font-weight: 600;">Small Incisions:</span> Tiny incisions are made around the shoulder to insert the arthroscope and surgical instruments.
                            </li>
                            <li>
                            <span style="font-weight: 600;">Removal of Bone Spurs:</span> Any excess bone or tissue causing compression is shaved down.
                            </li>
                            <li>
                            <span style="font-weight: 600;">Cleaning of the Subacromial Space:</span> Inflamed tissue, including bursitis, is removed.
                            </li>
                            <li>
                            <span style="font-weight: 600;">Closure:</span> The incisions are closed with stitches, and a dressing is applied.
                            </li>
                        </ul>
                     </p>

