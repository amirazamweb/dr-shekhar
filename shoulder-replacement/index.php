                    <h3>Conditions Treated by Shoulder Replacement</h3>

                    <p>
                    Shoulder replacement surgery is typically recommended for individuals experiencing severe shoulder pain or limited function due to:
                    <ul>
                        <li><span style="font-weight: 600;">Osteoarthritis:</span> Degeneration of the cartilage in the shoulder joint, leading to pain and stiffness.</li>
                        <li><span style="font-weight: 600;">Rheumatoid Arthritis:</span> An autoimmune disease that causes joint inflammation and damage.</li>
                        <li><span style="font-weight: 600;">Rotator Cuff Tears:</span> In some cases where the rotator cuff is severely damaged and cannot be repaired, shoulder replacement may be necessary.</li>
                        <li><span style="font-weight: 600;">Fractures:</span> Complex fractures that involve the shoulder joint and cannot be repaired with other methods.</li>
                        <li><span style="font-weight: 600;">Shoulder Instability:</span> Chronic dislocations or instability that leads to pain and loss of function.</li>
                    </ul>
                    </p>

                    <h3>Types of Shoulder Replacement</h3>
                    <p>
                    Dr. Shekhar Srivastav offers various types of shoulder replacement surgeries, depending on the patient's condition and needs:
                    <ul>
                        <li><span style="font-weight: 600;">Total Shoulder Replacement (TSA):</span> This is the most common type, in which both the ball (humeral head) and socket (glenoid) are replaced with prosthetic components.</li>
                        <li><span style="font-weight: 600;">Reverse Shoulder Replacement (RSA):</span> This procedure is typically recommended for patients with massive rotator cuff tears and arthritis. The ball and socket are reversed to allow the deltoid muscle to compensate for the lost rotator cuff function.</li>
                        <li><span style="font-weight: 600;">Partial Shoulder Replacement:</span> In this procedure, only the damaged part of the shoulder joint is replaced, while the remaining healthy portions are preserved.</li>
                    </ul>
                    </p>

